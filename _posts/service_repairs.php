			<h3 class="lead">What Can You Do When Your Hearing Aid Stops Functioning?</h3>
			<img src="../images/confused-unhappy.jpg" alt="Hearing aid repairs" class="img-responsive pull-left pull-img">
			<p>Hearing aids follow Murphy’s Rules of Reasoning. That is, when something is going to go wrong, it will happen at the most inopportune time. However, don’t assume the worst! There are some simple things you can do when your hearing aid doesn’t seem to work properly.</p>
			<p>The most common reason a hearing aid will not work is that it’s plugged with earwax or other debris. A plug of wax will prevent the sound from escaping from the speaker port of an in-the-ear aid or the earmold of a behind-the-ear aid, and you’ll think the aid is dead. However, you can remove the wax plug with the brush or wax loop that came with your hearing aids. If you don’t have cleaning tools, you can use the pointed end of a straight pin to <strong>gently</strong> dig the wax out.</p>
			<p>For those of you who have in-the-ear hearing aids that have wax guard filters installed, the cleaning process is slightly different. The filters are the tiny white plugs that fit into the end of the hearing aid that you insert into your ear canal. Replacement filters are in a 2-inch by 3-inch white or gray packet. When you snap open the top of the packet, you’ll find some black sticks.</p>
			<p>Remove one of the sticks and you’ll notice a small white piece on the tip. This is the replacement filter. Take out the old filter with the flattened end of the black stick by pushing it into the old filter and pulling it straight out of the aid. Then, flip over the stick and gently slide the replacement filter into the spot the plugged filter occupied. Usually, this is all that it takes to get your instrument functioning again.</p>
			<p>For behind-the-ear users, the problem is often a moisture plug inside of the tubing where the tube inserts into the earmold. You can break the moisture bubble by holding the upper portion of the aid firmly where the earmold tubing attaches to your instrument. Shake it two or three times, as if you were shaking down an old-fashioned thermometer. This force will usually break the moisture bubble and you’ll be back in business.</p>
