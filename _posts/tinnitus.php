				<p>Tinnitus can be caused by hundreds of conditions, such as ear infections, high blood pressure, heart disease or kidney disease. Sometimes individuals develop tinnitus through the use of prescription medications or as the result of a head injury. However, the most common cause in today’s world is exposure to loud noise. Most who served in the military or worked in heavy industry likely have tinnitus. If you listen to very loud music, you will probably have tinnitus. Often a feeling of plugged ears will accompany this condition. After noise exposure, the ears usually return to their former hearing levels, but the tinnitus may linger.</p>
				<p>There is a new theory about why one hears tinnitus. It is thought that the inner ear produces all sounds simultaneously and transmits them to the auditory reception area in the brain. At the same time, the brain sends identical signals back to the inner ear. In an undamaged ear, the two sounds cancel each other out.</p>
				<p>An inner ear that is damaged can no longer send this sound stream to the brain; however, the brain continues to send the sounds to the inner ear. Since there is no cancellation of the brain signals coming to it, the ear “hears” tinnitus.</p>
				<p>The tinnitus that I have described above is the most common type and is referred to as subjective tinnitus. A second, very rare form, known as objective tinnitus, has been documented in the research literature. The objective type is a perceived sensation of sound that occurs in the absence of external acoustic stimulation, but that can also be heard by an examiner placing a stethoscope over the patient’s external ear canal. Objective tinnitus often has an identifiable and curable cause, while subjective tinnitus does not.</p>
				<p>While subjective tinnitus cannot be cured, the use of hearing instruments is an effective way to manage it. Hearing aids increase the levels of softer sounds in the listening environment. When softer sounds are made louder, they will overshadow the tinnitus.</p>
